package com.tectone.web.model;

public class PageInitInfoBean {
	private UserInfoBean userSession;
	private boolean noSessionError;
	private String menuAuthCode;
	private boolean menuHierarhiError;
	
	public PageInitInfoBean() {
		this.userSession = null;
		this.noSessionError = false;
		this.menuAuthCode = "0";
		this.menuHierarhiError = false;
	}
	
	public UserInfoBean getUserSession() {
		return userSession;
	}
	public void setUserSession(UserInfoBean userSession) {
		this.userSession = userSession;
	}
	public boolean isNoSessionError() {
		return noSessionError;
	}
	public void setNoSessionError(boolean noSessionError) {
		this.noSessionError = noSessionError;
	}
	public String getMenuAuthCode() {
		return menuAuthCode;
	}
	public void setMenuAuthCode(String menuAuthCode) {
		this.menuAuthCode = menuAuthCode;
	}
	public boolean isMenuHierarhiError() {
		return menuHierarhiError;
	}
	public void setMenuHierarhiError(boolean menuHierarhiError) {
		this.menuHierarhiError = menuHierarhiError;
	}
}
